    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        PhuckhaoDAO pkDAO = new PhuckhaoDAO();
        List <Phuckhao> pkList = pkDAO.getListPhuckhao();
        String[] tblHead={"STT","MSSV","Họ Tên","Mã Lớp","Mã Môn","Cột Điểm","Mong Muốn","Lý Do","Tình Trạng"};
        DefaultTableModel model = new DefaultTableModel(tblHead,0);
        int stt = 0;
        for ( Phuckhao pk : pkList) {
            Vector vt = new Vector();
            stt++;
            vt.add(stt);
            vt.add(pk.getId().getMssv());
            vt.add(pk.getHoTen());
            vt.add(pk.getId().getMaLop());
            vt.add(pk.getId().getMaMh());
            vt.add(pk.getId().getCotDiem());
            vt.add(pk.getDiemMongMuon());
            vt.add(pk.getLyDo());
            vt.add(pk.getTinhTrang());
            model.addRow(vt);
        }
        jTable1.setModel(model);
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        jTable1.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(5).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(6).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(7).setCellRenderer(centerRenderer);
        jTable1.getColumnModel().getColumn(8).setCellRenderer(centerRenderer);
    }//GEN-LAST:event_jButton1MouseClicked

