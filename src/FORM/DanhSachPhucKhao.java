    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowC = model.getRowCount();
        PhuckhaoDAO pkDAO = new PhuckhaoDAO();
        for (int i = 0; i <rowC; i++)
        {
            Phuckhao pk = new Phuckhao();
            PhuckhaoId idpk = new PhuckhaoId();
            idpk.setMssv((String) model.getValueAt(i, 1));
            idpk.setMaLop((String) model.getValueAt(i, 3));
            idpk.setMaMh((String) model.getValueAt(i, 4));
            idpk.setCotDiem((String) model.getValueAt(i, 5));
            pk.setId(idpk);
            pk.setHoTen((String) model.getValueAt(i, 2));
            pk.setDiemMongMuon((String) model.getValueAt(i, 6));
            pk.setLyDo((String) model.getValueAt(i, 7));
            pk.setTinhTrang((String) model.getValueAt(i, 8));
            pkDAO.addPhuckhao(pk);
        }
    }//GEN-LAST:event_jButton2MouseClicked


